package com.galix.linguam.pojo;

public class Preferences {

	private int gameMode;
	private int nWordPerLevel;
	
	/**
	 * @return the gameMode
	 */
	public int getGameMode() {
		return gameMode;
	}
	/**
	 * @param gameMode the gameMode to set
	 */
	public void setGameMode(int gameMode) {
		this.gameMode = gameMode;
	}
	/**
	 * @return the nWordPerLevel
	 */
	public int getnWordPerLevel() {
		return nWordPerLevel;
	}
	/**
	 * @param nWordPerLevel the nWordPerLevel to set
	 */
	public void setnWordPerLevel(int nWordPerLevel) {
		this.nWordPerLevel = nWordPerLevel;
	}
	
}
